



n0 = int(input('Digite um número: '))
n1 = int(input('Digite um número: '))


s = n0 + n1
print('O resultado de {} + {} é: {}' .format(n0, n1, s))

s = n0 - n1
print('O resultado de {} - {} é: {}' .format(n0, n1, s))

s = n0 * n1
print('O resultado de {} * {} é: {}' .format(n0, n1, s))

s = n0 / n1
print('O resultado de {} / {} é: {}' .format(n0, n1, s))
